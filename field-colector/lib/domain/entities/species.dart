class Species {
  final String id;
  final String scientificName;
  final String commonName;
  final String branch;
  final String source;
  final String createdById;

  Species({
    required this.id,
    required this.scientificName,
    required this.commonName,
    required this.branch,
    required this.source,
    required this.createdById,
  });
}
